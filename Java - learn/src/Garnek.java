public class Garnek {
    final int srednica;
    final int wysokosc;
    String kolor;


    public Garnek(int srednica, int wysokosc) {
        this.srednica = srednica;
        this.wysokosc = wysokosc;
    }
    public Garnek(int srednica, int wysokosc, String kolor) {
        this.srednica = srednica;
        this.wysokosc = wysokosc;
        this.kolor = kolor;
    }
}
